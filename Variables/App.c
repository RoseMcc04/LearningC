#include <stdio.h>

int main() 
{
    /*
    There are four main types of variables in C:

    1. int
    2. float
    3. double
    4. char
    */

    int x = 7;
    float y = 16.5;

    // We can print out the integer like this...
    printf("%d\n", x);

    // We can print out the float like this...
    printf("%f\n", y);
}